#include<iostream>
using namespace std;
// for loop 
/*****************
int main() {

    int a;
    cin>>a;

    int sum = 0;
    for(int counter=1; counter <= a; counter++) {
        sum += counter;
    }
    cout<<sum<<endl;
    return 0;
}

***********************/

// while loop 
/********************
int main() {

    int n;
    cin>>n;

    while(n>0){
        cout<<n<<endl;
        cin>>n;
    }
    return 0;
}
**********************/


//do while loop 

int main(){

    int n;
    cin>>n;

    do {
        cout<<n<<endl;
        cin>>n;
    } while (n > 0);

    return 0;

}